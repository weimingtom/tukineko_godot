using System;

namespace tukineko
{
	public class NsButton {
		//private const long serialVersionUID = 1L;

		public int no;
		public int x;
		public int y;
		public int width;
		public int height;
		public int u;
		public int v;

		public NsButton(int no, int x, int y, int width, int height, int u, int v) {
			this.no = no;
			this.x = x;
			this.y = y;
			this.width = width;
			this.height = height;
			this.u = u;
			this.v = v;
		}
	}
}
